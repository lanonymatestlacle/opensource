import java.io.*;
import java.util.*;

public class Solution {

    public static void main(String[] args) {
        Scanner s=new Scanner(System.in);
        long x=s.nextLong();
        long y=s.nextLong();
        if(x>=y)
            System.out.println("YES");
        else
            System.out.println("NO");
    }
}
